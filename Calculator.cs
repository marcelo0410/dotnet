class Calculator
{
    public static String add(int num1, int num2)
    {
        return num1 + " + " + num2 + " = " + (num1 + num2);
    }

    public static String sub(int num1, int num2)
    {
        return num1 + " - " + num2 + " = " + (num1 - num2);
    }

    public static String mult(int num1, int num2)
    {
        return num1 + " * " + num2 + " = " + (num1 * num2);
    }

    public static String div(int num1, int num2)
    {
        return num1 + " / " + num2 + " = " + (num1 / num2);
    }
}